package InfosTravian;

public class JogadoresFarme {

	private String nomeJogador;
	private String linkJogador;
	AldeiasFarme aldeias;

	public AldeiasFarme getAldeias() {
		return aldeias;
	}

	public void setAldeias(AldeiasFarme aldeias) {
		this.aldeias = aldeias;
	}

	public String getNomeJogador() {
		return nomeJogador;
	}

	public void setNomeJogador(String nomeJogador) {
		this.nomeJogador = nomeJogador;
	}

	public String getLinkJogador() {
		return linkJogador;
	}

	public void setLinkJogador(String linkJogador) {
		this.linkJogador = linkJogador;
	}

}
